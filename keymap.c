oled_rotation_t oled_init_user(oled_rotation_t rotation) {
    if (is_keyboard_master()) {
        return OLED_ROTATION_270;
    } else {
        return OLED_ROTATION_180;
    }

    return rotation;
}

bool oled_task_user(void) {
    if (is_keyboard_master()) {
        oled_clear();
        oled_set_cursor(0,2);

        switch (get_highest_layer(layer_state)) {
        case _QWERTY:
            oled_write_raw_P(qwerty_layer, sizeof(qwerty_layer));
            break;
        case _LOWER:
            oled_write_raw_P(lower_layer, sizeof(lower_layer));
            break;
        case _RAISE:
            oled_write_raw_P(raise_layer, sizeof(raise_layer));
            break;
        case _ADJUST:
            oled_write_raw_P(adjust_layer, sizeof(adjust_layer));
            break;
        default:
            oled_write_ln_P(PSTR("Undefined"), false);
        }
        oled_set_cursor(0,10);
        led_t led_state = host_keyboard_led_state();
        if(led_state.caps_lock) {
            oled_write_raw_P(capslock_layer, sizeof(capslock_layer));
        }
        // oled_write_P(led_state.caps_lock ? PSTR("CAPS") : PSTR("    "), false);
    } else {
        render_logo();
        oled_scroll_left();
    }
    return false;
}
